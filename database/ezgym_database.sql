CREATE DEFINER=`root`@`localhost` PROCEDURE `find_by_athlete_name`(
	IN p_athlete_name VARCHAR(45),
    IN p_athlete_surname VARCHAR(45))
BEGIN
	SELECT
        u.name,
        u.surname,
        a.id_athlete AS athlete_id,
        w.workout_id,
        w.repete_weeks,
        ws.session_id,
        ws.day_of_week,
        wse.exercise_name,
        wse.sets,
        wse.reps,
        wse.rest_time,
        wse.type,
        wse.notes
    FROM users u
    JOIN athletes a
        ON a.id_athlete = u.id
    JOIN workouts w
        ON w.athlete_id = a.id_athlete
    JOIN wk_plan_sessions wps
        ON wps.workout_id = w.workout_id
    JOIN workout_sessions ws
        ON ws.session_id = wps.session_id
    LEFT JOIN wk_session_exercises wse
        ON wse.session_id = ws.session_id
    WHERE u.role = 'ATHLETE'
      AND u.name = p_athlete_name
      AND (p_athlete_surname IS NULL OR p_athlete_surname = '' OR u.surname = p_athlete_surname)
      AND w.workout_id = (
            SELECT MAX(w2.workout_id)
            FROM workouts w2
            WHERE w2.athlete_id = a.id_athlete
      )
    ORDER BY ws.session_id, wse.exercise_name;
END ;;
